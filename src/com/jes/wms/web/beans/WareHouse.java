package com.jes.wms.web.beans;

import java.util.Date;

public class WareHouse {
	
	private Integer iKey;
	private String description;
	private Boolean status;
	private String org;
	private String createBy;
	private Date createDate;
	private String updateBy;
	private Date updateDate;
	private String code;
	private String name;
	private String qty_Confine;
	
	public WareHouse(){
		
		
	}

	WareHouse(Integer iKey, String description, Boolean status, String org,
			String createBy, Date createDate, String updateBy, Date updateDate,
			String code, String name, String qty_Confine) {
		super();
		this.iKey = iKey;
		this.description = description;
		this.status = status;
		this.org = org;
		this.createBy = createBy;
		this.createDate = createDate;
		this.updateBy = updateBy;
		this.updateDate = updateDate;
		this.code = code;
		this.name = name;
		this.qty_Confine = qty_Confine;
	}

	public Integer getiKey() {
		return iKey;
	}

	public void setiKey(Integer iKey) {
		this.iKey = iKey;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public Boolean getStatus() {
		return status;
	}

	public void setStatus(Boolean status) {
		this.status = status;
	}

	public String getOrg() {
		return org;
	}

	public void setOrg(String org) {
		this.org = org;
	}

	public String getCreateBy() {
		return createBy;
	}

	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	public String getUpdateBy() {
		return updateBy;
	}

	public void setUpdateBy(String updateBy) {
		this.updateBy = updateBy;
	}

	public Date getUpdateDate() {
		return updateDate;
	}

	public void setUpdateDate(Date updateDate) {
		this.updateDate = updateDate;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getQty_Confine() {
		return qty_Confine;
	}

	public void setQty_Confine(String qty_Confine) {
		this.qty_Confine = qty_Confine;
	}
	
	
	
	
	
	
	

}
