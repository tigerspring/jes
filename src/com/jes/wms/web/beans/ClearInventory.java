package com.jes.wms.web.beans;

import java.util.Date;

public class ClearInventory {
	private Integer iKey;
	private String assetcode;
	private String assetname;
	private String type;
	private String unit;
	private Date selldate;
	private Double price;
	private Integer realquantity;
	private Integer accoutquantity;
	private String location;
	private Boolean status;
	private String responsibility;
	private Integer clearquantity;
	private String note;
	
	public ClearInventory(){
		
		
		
	}

	ClearInventory(Integer iKey, String assetcode, String assetname,
			String type, String unit, Date selldate, Double price,
			Integer realquantity, Integer accoutquantity, String location,
			Boolean status, String responsibility, Integer clearquantity,
			String note) {
		super();
		this.iKey = iKey;
		this.assetcode = assetcode;
		this.assetname = assetname;
		this.type = type;
		this.unit = unit;
		this.selldate = selldate;
		this.price = price;
		this.realquantity = realquantity;
		this.accoutquantity = accoutquantity;
		this.location = location;
		this.status = status;
		this.responsibility = responsibility;
		this.clearquantity = clearquantity;
		this.note = note;
	}

	public Integer getiKey() {
		return iKey;
	}

	public void setiKey(Integer iKey) {
		this.iKey = iKey;
	}

	public String getAssetcode() {
		return assetcode;
	}

	public void setAssetcode(String assetcode) {
		this.assetcode = assetcode;
	}

	public String getAssetname() {
		return assetname;
	}

	public void setAssetname(String assetname) {
		this.assetname = assetname;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getUnit() {
		return unit;
	}

	public void setUnit(String unit) {
		this.unit = unit;
	}

	public Date getSelldate() {
		return selldate;
	}

	public void setSelldate(Date selldate) {
		this.selldate = selldate;
	}

	public Double getPrice() {
		return price;
	}

	public void setPrice(Double price) {
		this.price = price;
	}

	public Integer getRealquantity() {
		return realquantity;
	}

	public void setRealquantity(Integer realquantity) {
		this.realquantity = realquantity;
	}

	public Integer getAccoutquantity() {
		return accoutquantity;
	}

	public void setAccoutquantity(Integer accoutquantity) {
		this.accoutquantity = accoutquantity;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public Boolean getStatus() {
		return status;
	}

	public void setStatus(Boolean status) {
		this.status = status;
	}

	public String getResponsibility() {
		return responsibility;
	}

	public void setResponsibility(String responsibility) {
		this.responsibility = responsibility;
	}

	public Integer getClearquantity() {
		return clearquantity;
	}

	public void setClearquantity(Integer clearquantity) {
		this.clearquantity = clearquantity;
	}

	public String getNote() {
		return note;
	}

	public void setNote(String note) {
		this.note = note;
	}
	
	
	
	
	
	
	
	
	
	
	

}
